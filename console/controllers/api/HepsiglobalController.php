    /**
     * 订单状态
     * @return void
     */
    public function actionOrderStatus()
    {
        $where = [
            'source' => Base::PLATFORM_HEPSIGLOBAL,
            'order_status' => [Order::ORDER_STATUS_FINISH,Order::ORDER_STATUS_REFUND,Order::ORDER_STATUS_CANCELLED]
        ];
        $order_lists = Order::find()->where($where)
            ->andWhere(['<', 'exec_status_time', time() - 15 * 24 * 60 * 60])
            ->andWhere(['>', 'add_time', time() - 6 * 30 * 24 * 60 * 60])
            ->all();
        foreach ($order_lists as $order) {
            $shop = Shop::findOne($order['shop_id']);
            $api_service = FApiService::factory($shop);
            try {
                $result = $api_service->getOrderInfo($order['relation_no']);
            } catch (\Exception $e) {
                echo '###'.$order['shop_id'] .",". $order['order_id']. ','.$e->getMessage()."\n";
                continue;
            }
            $is_refund = false;
            $is_cancel = false;
            $data_time = '';
            foreach ($result['order_items'] as $v) {
                if ($v['status'] == 'cancel') {
                    $is_cancel = true;
                }

                if ($v['status'] == 'refund') {
                    $is_refund = true;
                }
                $data_time = empty($v['due_date'])?strtotime($v['updated_at']):strtotime($v['due_date']);
            }

            $financial_operation = FinancialPeriodRollover::find()->where(['relation_no' => $order['relation_no'],'shop_id'=>$order['shop_id']])->all();
            $operations = ArrayHelper::getColumn($financial_operation,'operation');
            $info = $result;
            $data = [];
            //销售金额
            if(!in_array('income',$operations)) {
                $data[] = [
                    'operation'=>'income',
                    'amount' => $info['base_price_total'] + $info['commission_fee_total'] + $info['commission_vat_fee_total'] + $info['marketplace_service_fee_total'] + $info['marketplace_service_vat_fee_total'],
                ];
            }

            //佣金
            if(!in_array('commission',$operations)) {
                $data[] = [
                    'operation'=>'commission',
                    'amount' => -($info['commission_fee_total'] + $info['commission_vat_fee_total']),
                ];
            }

            //手续费
            if(!in_array('mp_service',$operations)) {
                $data[] = [
                    'operation'=>'mp_service',
                    'amount' => -($info['marketplace_service_fee_total'] + $info['marketplace_service_vat_fee_total']),
                ];
            }

            /**
            销售金额：base_price_total + commission_fee_total + commission_vat_fee_total + marketplace_service_fee_total + marketplace_service_vat_fee_total

            佣金：- commission_fee_total + commission_vat_fee_total

            手续费： - marketplace_service_fee_total + marketplace_service_vat_fee_total

            退款金额	：- base_price_total + commission_fee_total + commission_vat_fee_total + marketplace_service_fee_total + marketplace_service_vat_fee_total

            退款运费 ：- logistic_fee_total + service_fee_total

            退款用户运费 ：- customs_fee_total

            退款佣金	：commission_fee_total + commission_vat_fee_total
             */

            //退款状态
            if ($is_refund) {
                //if ($order['order_status'] != Order::ORDER_STATUS_REFUND) {
                    //退款金额
                    if(!in_array('refund',$operations)) {
                        $data[] = [
                            'operation'=>'refund',
                            'amount' => -($info['base_price_total'] + $info['commission_fee_total'] + $info['commission_vat_fee_total'] + $info['marketplace_service_fee_total'] + $info['marketplace_service_vat_fee_total']),
                        ];

                        //退款运费
                        if(!in_array('refund_shipping',$operations)) {
                            $data[] = [
                                'operation'=>'refund_shipping',
                                'amount' => -($info['logistic_fee_total'] + $info['service_fee_total']),
                            ];
                        }
                        //退款用户运费
                        if(!in_array('refund_customs',$operations)) {
                            $data[] = [
                                'operation'=>'refund_customs',
                                'amount' => -$info['customs_fee_total'] ,
                            ];
                        }
                        //退款佣金
                        if(!in_array('invoice_diff',$operations)) {
                            $data[] = [
                                'operation' => 'invoice_diff',
                                'amount' => $info['commission_fee_total'] + $info['commission_vat_fee_total'],
                            ];
                        }
                    }
                //}
            } else {
                if ($is_cancel) {
                    if ($order['order_status'] != Order::ORDER_STATUS_CANCELLED) {
                        (new OrderService())->cancel($order['order_id'], 9, '系统自动取消');
                    }
                    continue;
                }
            }

            if(!empty($data)) {
                $f_data = [];
                foreach ($data as $api_val_v) {
                    $f_data[] = [
                        'platform_type' => $order['source'],
                        'shop_id' => $order['shop_id'],
                        'date' => $data_time,
                        'identifier' => '-1',
                        'operation' => $api_val_v['operation'],
                        'amount' => $api_val_v['amount'],
                        'collection_time' => 0,
                        'currency' => 'USD',
                        'relation_no' => (string)$order['relation_no'],
                        'order_id' => (string)$order['order_id'],
                        'add_time' => time(),
                        'update_time' => time(),
                    ];
                }
                $add_columns = [
                    'platform_type',
                    'shop_id',
                    'date',
                    'identifier',
                    'operation',
                    'amount',
                    'collection_time',
                    'currency',
                    'relation_no',
                    'order_id',
                    'add_time',
                    'update_time'
                ];
                FinancialPeriodRollover::getDb()->createCommand()->batchIgnoreInsert(FinancialPeriodRollover::tableName(), $add_columns, $f_data)->execute();
                (new OrderSettlementService())->orderSettlement($order['order_id']);
            }
            $order->exec_status_time = time();
            $order->save();
            echo $order['shop_id'] .",". $order['order_id'] .",".$is_refund.",".$is_cancel."\n";
        }
    }

    /**
     * @param $shop_id
     * @param $page
     * @return void
     * @throws \yii\base\Exception
     */
    public function actionActiveGoods($shop_id,$page = 0)
    {
        $shop = Shop::findOne($shop_id);
        if ($shop['platform_type'] != Base::PLATFORM_HEPSIGLOBAL) {
            return;
        }
        $api_service = FApiService::factory($shop);
        $limit = 1000;
        echo '##shop_id :'. $shop_id."\n";
        $i=0;
        while (true) {
            echo '##Page :'.$page."\n";
            $offset = $page * $limit;
            $page ++;
            $result = $api_service->getActiveProductsLists($offset, $limit);
            if(empty($result) || empty($result['data'])){
                break;
            }
            $i++;
            foreach ($result['data'] as $hep_goods) {
                $i++;
                foreach ($hep_goods['variants'] as $hep_variants) {
                    $goods_shop = GoodsShop::find()->where(['shop_id' => $shop_id, 'platform_sku_no' => $hep_variants['sku']])->one();
                    if (empty($goods_shop)) {
                        echo $i. ',' .$hep_variants['sku'] . ' 不存在' . "\n";
                        continue;
                    }
                    $goods_shop->platform_goods_opc = (string)$hep_variants['product_id'];
                    $goods_shop->platform_goods_id = (string)$hep_variants['id'];
                    $goods_shop->save();
                    if (empty($hep_goods['marketplace_products']) || count($hep_goods['marketplace_products']) < 1) {
                        echo $i. ',' .$hep_variants['sku'] . ' 不在市场售卖' . "\n";
                        continue;
                    }
                    echo $i. ',' .$hep_variants['sku'] . "\n";
                }
            }

            if(count($result['data']) < $limit) {
                break;
            }
        }
    }

